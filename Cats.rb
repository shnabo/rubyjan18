


class Cat
  def initialize(name, preferred_food, meal_time)
    @name = name
    @preferred_food = preferred_food
    @meal_time = meal_time
  end
  def eat_at
    @mealtime = @mealtime
    puts " #{@meal_time} PM"
  end
  def meow
    @mealtime = @mealtime
    puts "My name is #{name}" + "i love to eat #{preferred_food}" +"at #{meal_time}"
  end
end
